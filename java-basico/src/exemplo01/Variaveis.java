package exemplo01;

/**
 * programa para mostrar como utilizar variaveis
 * @author nlanaro
 *@since 04/02/2021
 */
public class Variaveis {

	/**
	 * metodo principal para executar o programa
	 */
	public static void main(String[] args) {
		// variavel tipo inteiro
		int idade;
		
		//variavel tipo string
		String nome;
		
		//variavel tipo double
		double valor;
		
		//atribuindo valores para variavel
		idade = 25;
		nome = "Nathalia Lanaro";
		valor = 155.98;
		
		//exibindo as variaveis
		
		System.out.println("Minha variavel Idade: "+ idade + ".");
		System.out.println("Minha variavel nome: "+ nome + ".");
		System.out.println("Minha variavel valor: "+ valor + ".");
		
	}

}
