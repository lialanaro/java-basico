
package exercicios;

/**
 * programa para criar variaveis, atribuir valores e apresentar no console
 * 
 * @since 04/02/2021
 * @author nathalia lanaro
 *
 */
public class Exercicio05 {

	/*
	 * metodo que executa o programa
	 */
	public static void main(String[] args) {
		// declarando variaveis
		int idade;
		double peso;
		double altura;

		// atribuindo valores para as variaves
		idade = 25;
		peso = 50.9;
		altura = 1.65;

		// apresentando os valores no console
		System.out.println("Minha idade é: " + idade);
		System.out.println("Minha peso é: " + peso);
		System.out.println("meu altura é: " + altura);

	}

}
