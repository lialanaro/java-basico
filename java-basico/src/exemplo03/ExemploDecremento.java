package exemplo03;

public class ExemploDecremento {
	public static void main(String[] args) {
		// declarando variaveis
		int numero;

		numero = 1;
		System.out.println(numero); // exibindo o valor 1

		numero = numero - 1; // decremento
		System.out.println(numero); // exibindo valor 0

		numero = 10;

		numero--;

		System.out.println(numero); // exibindo o valor 9

		numero--; // decremento
		numero--;// decremento
		System.out.println(numero); // exibindo 7

	}

}
